package com.mattzm.gungale.property;

public class DamageProperty {
    public final float headDamage;
    public final float bodyDamage;
    public final float legsDamage;
    
    public DamageProperty(float headDamage, float bodyDamage, float legsDamage) {
        this.headDamage = headDamage;
        this.bodyDamage = bodyDamage;
        this.legsDamage = legsDamage;
    }
}
